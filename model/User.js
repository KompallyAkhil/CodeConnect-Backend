import mongoose from "mongoose";
const registerSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { collection: "CodeConnect" }
);

const Register = mongoose.model("CodeConnect", registerSchema);
export default Register;
